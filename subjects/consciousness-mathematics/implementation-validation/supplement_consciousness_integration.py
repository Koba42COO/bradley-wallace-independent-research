        i = 2
        while i * i <= n:
            if n % i:
                i += 1
            else:
                n //= i
                factors.append(i)
        if n > 1:
            factors.append(n)

        return {
            'number': n,
            'prime_factors': factors,
            'factorization_string': ' Ã— '.join(map(str, factors)),
            'is_prime': len(factors) == 1
        }

